let totalPerguntas = perguntas.length;
let perguntasRespondidas = 0;
let respostasCorretas = 0;
let respostasSelecionadas = [];

function comecarQuiz() {
  perguntasRespondidas = 0;
  respostasCorretas = 0;
  respostasSelecionadas = [];
  exibirPergunta();
  exibirBotoesNavegacao();
}


function exibirPergunta() {
  let perguntaElemento = document.getElementById("pergunta");
  perguntaElemento.style.display = "block";

  let pergunta = perguntas[perguntasRespondidas];
  perguntaElemento.querySelector("h3").textContent = "Pergunta " + (perguntasRespondidas + 1) + ":";
  perguntaElemento.querySelector("p").textContent = pergunta.pergunta;

  let opcoesRespostaElemento = perguntaElemento.querySelector("#opcoes-resposta");
  opcoesRespostaElemento.innerHTML = "";

 
  for (let i = 0; i < pergunta.opcoes.length; i++) {
    let opcao = pergunta.opcoes[i];
    let li = document.createElement("li");
    let input = document.createElement("input");
    input.type = "radio";
    input.name = "pergunta";
    input.value = String.fromCharCode(97 + i); // A: 97, B: 98, C: 99, D: 100

    // Verifica se essa opção estava selecionada anteriormente
    if (respostasSelecionadas[perguntasRespondidas] === input.value) {
      li.classList.add("selecionado");
      input.checked = true; // Marca o input como selecionado
    }

    input.addEventListener("change", function() {
      respostasSelecionadas[perguntasRespondidas] = input.value; // Atualiza a resposta selecionada
    });

    li.appendChild(input);
    li.appendChild(document.createTextNode(opcao));
    opcoesRespostaElemento.appendChild(li);
  }
}
  
  function exibirBotoesNavegacao() {
    let botoesNavegacao = document.getElementById("botoes-navegacao");
    botoesNavegacao.style.display = "block";
  
    let botaoAnterior = document.getElementById("pergunta-anterior");
    let botaoProxima = document.getElementById("proxima-pergunta");
  
    botaoAnterior.style.display = perguntasRespondidas === 0 ? "none" : "inline-block";
    botaoProxima.style.display = perguntasRespondidas === totalPerguntas - 1 ? "none" : "inline-block";
  }
  
 
function perguntaAnterior() {
  if (perguntasRespondidas > 0) {
    perguntasRespondidas--;
    exibirPergunta();
    exibirBotoesNavegacao();
  }
}
  
  function proximaPergunta() {
    if (perguntasRespondidas < totalPerguntas - 1) {
      perguntasRespondidas++;
      exibirPergunta();
      exibirBotoesNavegacao();
    }
  }
 
function verificarRespostas() {
  respostasCorretas = 0;

  for (let i = 0; i < perguntasRespondidas; i++) {
    let resposta = respostasSelecionadas[i];
    let pergunta = perguntas[i];

    if (resposta === pergunta.resposta) {
      respostasCorretas++;
    }
  }

  let resultado = 'Você acertou ' + respostasCorretas + ' de ' + perguntasRespondidas + ' questões.';
  document.getElementById('respostas-container').textContent = resultado;
}

function iniciarQuiz() {
  comecarQuiz();
  exibirBotoesNavegacao();
}

function reiniciarQuiz() {
  perguntasRespondidas = 0;
  respostasCorretas = 0;
  respostasSelecionadas = [];
  comecarQuiz();
  document.getElementById('respostas-container').textContent = '';
}
